#include <stdio.h>

int	ft_fibonacci(int index)
{
	if (index < 0)
		return (-1);
	if (index == 0)
		return (0);
	if (index == 1)
		return (1);
	return (ft_fibonacci(index - 1) + ft_fibonacci(index - 2));
}

int	main(void)
{
	int	i;

	i = 7;
	printf("Ultimo elemento de %d elementos: %d\n", i, ft_fibonacci(i));
	return (0);
}
