#include <unistd.h>
//teste
//[t],[e],[s],[t],[e],[\0]
void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(void)
{
	ft_putstr("Testando 1, 2, 3...\n");
	return (0);
}
