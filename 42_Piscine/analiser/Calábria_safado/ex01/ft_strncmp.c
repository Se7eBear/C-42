#include <unistd.h>
#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && i < n)
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}

int main()
{
	char ar1[] = {"Ola"};
	char ar2[] = {"Ola"};
	int number = 4;
	printf("%d\n", ft_strncmp(ar1, ar2, number));
}
